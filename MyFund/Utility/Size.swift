import UIKit
import Foundation


public func SCREEN_WIDTH() -> CGFloat {
    return UIScreen.mainScreen().bounds.size.width
}

public func SCREEN_HEIGHT() -> CGFloat {
    return UIScreen.mainScreen().bounds.size.height
}

public func TABBAR_HEIGHT() -> CGFloat {
    return 49
}